CREATE TABLE employees (
  id NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  name VARCHAR2(255) NOT NULL,
  employee_no VARCHAR2(20) UNIQUE,
  department VARCHAR2(100),
  email VARCHAR2(255) UNIQUE,
  phone VARCHAR2(50),
  status VARCHAR2(50) DEFAULT 'ACTIVE',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE appointments (
  id NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  user_id NUMBER(19) NOT NULL,
  service VARCHAR2(255) NOT NULL,
  vehicle VARCHAR2(255) NOT NULL,
  date DATE NOT NULL,
  time_col VARCHAR2(8) NOT NULL,
  status VARCHAR2(20) NOT NULL DEFAULT 'PENDING',
  notes VARCHAR2(1000),
  technician VARCHAR2(255),
  employee_id NUMBER(19),
  estimated_hours NUMBER(5,2),
  actual_hours NUMBER(5,2) DEFAULT 0,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT fk_appointments_user FOREIGN KEY (user_id) REFERENCES users(id),
  CONSTRAINT fk_appointments_employee FOREIGN KEY (employee_id) REFERENCES employees(id)
);

CREATE TABLE time_logs (
  id NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  employee_id NUMBER(19) NOT NULL,
  appointment_id NUMBER(19) NOT NULL,
  date DATE NOT NULL,
  hours NUMBER(5,2) NOT NULL,
  description VARCHAR2(2000) NOT NULL,
  status VARCHAR2(20) NOT NULL DEFAULT 'COMPLETED',
  is_billable NUMBER(1) DEFAULT 1,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT fk_time_logs_employee FOREIGN KEY (employee_id) REFERENCES employees(id),
  CONSTRAINT fk_time_logs_appointment FOREIGN KEY (appointment_id) REFERENCES appointments(id)
);